			<strong style="font-size:30px; color:black">About Blood</strong>
			<br />
			<strong style="font-size:26px; color:#CC0000">Blood</strong> is a bodily fluid in animals that delivers necessary 				substances such as nutrients and oxygen to the cells and transports metabolic waste products away from those same cells. When it reaches the lungs, gas exchange occurs when carbon dioxide is diffused out of the blood into the alveoli and oxygen is diffused into the blood. This oxygenated blood is pumped to the left hand side of the heart in the pulmonary vein and enters the left atrium. From here it passes through the bicuspid valve, through the ventricle and taken all around the body by the aorta. Blood contains antibodies, nutrients, oxygen and much more to help the body work.
			<br /><br />

			<strong style="font-size:30px; color:black">Tips for a Successful Donation</strong>
			<br />

			By following a few recommendations before, during and after your blood donation can help you make your donation experience as safe, successful and pleasant as possible.
			<br /><br />
			<strong style="font-size:22px; color:#003359">Before Your Donation</strong>
			<br />
			<ul>
			<li><img align="right" alt="Blood Donation Tip - Eat Healthy" class="image image-thumbnail " height="175" width="240" src="images/veg.jpg"  />Maintain a healthy iron level in your diet by eating, such as red meat, fish, poultry, beans, spinach, iron-fortified cereals and raisins.</li>
			<li>Get a good night's sleep.</li>
			<li>Drink an extra 16 oz. of water or nonalcoholic fluids before the donation.</li>
			<li>Eat a healthy meal before your donation.&nbsp;Avoid fatty foods, such as hamburgers, fries or ice cream before donating. (Fatty foods can affect the tests we do on your blood. If there is too much fat in your blood, your donation cannot be tested for infectious diseases and the blood will not be used for transfusion.)</li>
			</ul>

			<strong style="font-size:22px; color:#003359">During Your Donation</strong>
			<ul type="disc">
			<li>Wear clothing with sleeves that can be raised above the elbow.</li>
			<li>Let the person taking your blood know if you have a preferred arm and show them any good veins that have been used successfully in the past to draw blood.</li>
			<li>Relax, listen to music, talk to other donors or read during the donation process.</li>
			<li>Take the time to enjoy a snack and a drink in the refreshments area immediately after donating.</li>
			</ul>


			<strong style="font-size:22px; color:#003359">After Your Donation</strong>

			<ul>
			<li>Drink an extra four (8 ounce) glasses of liquids and avoid alcohol over <em>the next 24 hours</em>.</li>
			<li>Remove the wrap bandage (if you had one put on your arm) within <em>the next hour</em>.</li>
			<li>Keep the strip bandage on for <em>the next several hours</em>.</li>
			<li>To avoid a skin rash, clean the area around the strip bandage with soap and water.</li>
			<li>Do not do any heavy lifting or vigorous exercise for <em>the rest of the day</em>.</li>
			<li>If the needle site starts to bleed, apply pressure to it and raise your arm straight up for about 5-10 minutes or 		until bleeding stops.</li>
			<li>If you experience dizziness or lightheadedness after donation, stop what you are doing and sit down or lie down until you feel better. Avoid performing any activity where fainting may lead to injury for at least 24 hours.
			</ul>

