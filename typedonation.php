    <h1>Types of Donations</h1>
    <div class="bodyCopy">
        <strong style="font-size:26px; color:#CC0000">You</strong> may not know it, but you can donate more than just blood. What you think of as blood donation is called "Whole Blood." There are many components of blood that are used by themselves, and you can give them! Read below to learn about all the different ways you can give.<br /><br />
       <strong> <dt><div class="Ttext" style="font-size:20px; color:#00748D;">Whole Blood Donation</div></dt></strong>
            <dd><div class="Dtext">
                When you give blood that contains all of its components, that is called a whole blood donation. This is the most common type of blood donation. During processing, each whole blood unit may be separated into up to four blood components: red blood cells, platelets, plasma and cryoprecipitate. In order to give whole blood, donors must meet all routine donor eligibility criteria. The donation process generally takes about 5 - 10 minutes.<br /><br />
				Whole blood donors may give at mobile drives or at one of our convenient donor centers every 56 days.
            </div></dd><br />
             <strong> <dt><div class="Ttext" style="font-size:20px; color:#00748D;">Automated Donations</div></dt></strong>
        <dd><div class="DText">Automated donation is a process that allows you to give more of the blood components our patients need. An automated process called “apheresis” is used to separate blood components while blood is being drawn. The selected component is collected and saved, while the rest of your blood is immediately returned to you.<br /><br />
Automated donation helps you maximize your ability to help others by providing more of the blood components needed.
</div></dd><br />
<strong> <dt><div class="Ttext" style="font-size:20px; color:#00748D;">Platelet Donations</div></dt></strong>
        <dd><div class="DText">Platelets are the component that aids in clotting, which benefits patients who develop bleeding disorders from leukemia, cancer therapy or open heart surgery.<br /><br />
It takes six or more whole blood donors to produce just one unit of transfusable platelets. With automated donation technology, you can give up to three concentrated platelet transfusions with one donation. Because your body replenishes platelets rapidly and you keep your red cells, you can donate platelets up to 24 times per year.<br /><br />
Platelet donors must meet the same requirements as routine whole blood donors, which means most healthy adults can give platelets. Our patients most often need platelets from type A and Type AB blood donors.<br /><br />
</div></dd>
<strong> <dt><div class="Ttext" style="font-size:20px; color:#00748D;">Double Red Cell Donation (2RB)</div></dt></strong>
        <dd><div class="DText">
More than 69% of all transfusions require red blood cells, which carry the hemoglobin and oxygen in the body. Anemic patients, transplant patients, heart surgery patients or accident and trauma patients need red cells, usually in multiple units. Because of automated donation technology, you can safely give two full transfusable doses of red cells in a single donation. The procedure takes just 20-30 minutes longer than a whole blood donation and can be performed every 112 days (every 16 weeks).<br /><br />
Double red cell donors must meet the same requirements as routine whole blood donors, with a few exceptions. The criterion differs depending on one’s gender, height, weight, hematocrit level and the device to be used for your donation. P
        </div></dd><br />
        <strong> <dt><div class="Ttext" style="font-size:20px; color:#00748D;">Plasma Donations</div></dt></strong>

        <dd><div class="DText">Plasma, the liquid portion of the blood, is needed in organ transplantation and to replace blood volume for trauma or burn victims. During an apheresis plasma donation, the donor can give three times more plasma than during a whole blood donation.<br /><br />
The entire collection process takes approximately 60 minutes and can be performed as often as every four weeks.<br /><br />
Donors with blood type AB not only have a rare blood type (only four percent of the population is AB), but they also have a rare opportunity to save lives in a special way. Donors with AB blood types have "universal donor plasma", so it can be transfused safely into a patient of any blood group.
</div></dd><br />
        <strong> <dt><div class="Ttext" style="font-size:20px; color:#00748D;">Autologous Donation</div></dt></strong>
            <dd><div class="Dtext">
                When scheduling non-emergency surgery or medical procedures that may require a blood transfusion, patients may elect to have blood drawn before the procedure in order to be transfused with their own blood. This is called autologous donation (‘auto’ meaning self).<br /><br />
                An autologous donation requires an Autologous Request Form completed and signed by a physician. An appointment is required and your donation must occur at least 30 days prior and no less than 5 days before your scheduled medical procedure.<br />
            </div></dd><br />
        <strong> <dt><div class="Ttext" style="font-size:20px; color:#00748D;">Directed Donation</div></dt></strong>
            <dd><div class="Dtext">
                A physician and/or patient may specify a list of people (directed donors) to donate blood for his/her use during a scheduled surgery or medical procedure. The unit is reserved for that specific patient’s use.<br /><br />
				All directed donors must meet routine donor eligibility criteria. Directed donations require a physician's order.<br /><br />
              
            </div></dd>
